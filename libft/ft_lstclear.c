#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*tmp;

	if (!lst || !del)
		return ;
	tmp = *lst;
	while (tmp)
	{
		*lst = tmp;
		tmp = tmp->next;
		ft_lstdelone(*lst, del);
	}
	*lst = NULL;
}
